   <script>
    const slider = document.querySelector('.participants-cards_slider');
    const slides = document.querySelectorAll('.participants-cards_slider-list');
    const slideWidth = document.querySelector('.participants-cards_slider-list').clientWidth;
    let slideIndex = 0;

    function showSlide(index) {
        slides.forEach((slide, i) => {
            if (i === index) {
                slide.classList.add('active');
            } else {
                slide.classList.remove('active');
            }
        });
    }

    function updatePageIndicator() {
        const currentPage = slideIndex + 1;
        const totalPages = slides.length;
        document.querySelector('.page-indicator').textContent = `${currentPage} / ${totalPages}`;
    }

    function nextSlide() {
        if (slideIndex < slides.length - 1) {
            slideIndex++;
        } else {
            slideIndex = 0;
        }
        showSlide(slideIndex);
        updatePageIndicator();
    }

    function prevSlide() {
        if (slideIndex > 0) {
            slideIndex--;
        } else {
            slideIndex = slides.length - 1;
        }
        showSlide(slideIndex);
        updatePageIndicator();
    }

    showSlide(slideIndex);
    updatePageIndicator();

       function startAutoSlide() {
        slideInterval = setInterval(nextSlide, 4000); // Переключение каждые 2 секунды
    }

    function stopAutoSlide() {
        clearInterval(slideInterval);
    }

    startAutoSlide(); // Начать автоматическое переключение слайдов

</script>
